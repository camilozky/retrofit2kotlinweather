    val hdurl: String? = null
    @SerializedName("media_type")
    val media_type: String? = null
    @SerializedName("service_version")
    val service_version: String? = null
    @SerializedName("title")
    val title: String? = null
    @SerializedName("url")
    val url: String? = null
}
